SELECT COUNT(GENERATION) AS COUNT, GENERATION
FROM GENERATE
WHERE ID NOT IN(SELECT PARENT_ID FROM GENERATE WHERE PARENT_ID IS NOT NULL)
GROUP BY GENERATION