WITH RECURSIVE GENERATE AS (
    SELECT ID, PARENT_ID, 1 AS GENERATION
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
    
    UNION ALL
    
    SELECT o.ID, o.PARENT_ID, p.GENERATION+1 AS GENERATION
    FROM ECOLI_DATA o
    JOIN GENERATE p
    ON o.PARENT_ID = p.ID
)

